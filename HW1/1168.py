# Коли щодня витрачати на опалення 9 кг вугілля, то його вистачить на
# 72 дні. На скільки днів вистачить вугілля, коли щодня витрачати 8 кг?

coal_per_day = 9
days = 72

total_coal = days * coal_per_day
print(f"Всього є {total_coal} кг вугілля")

print(f"Якщо витрачати 8 кг вугілля в день, то його вистачить на {int(total_coal / 8)} днів")
