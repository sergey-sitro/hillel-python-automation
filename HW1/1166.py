# З урожаю льону на дослідній ділянці одержали 244 кг волокна льону,
# а насіння - в 2 рази менше. Скільки кілограмів насіння одержали?

total_flax_fibers = 244
print(f"Одержано волокна льону: {total_flax_fibers} кг.")

flax_seed_received = int(total_flax_fibers / 2)
print(f"Одержано насіння льону: {flax_seed_received} кг.")
